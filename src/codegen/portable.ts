function commandDriver(
  json: DatasheetJson,
  commands: Command[],
  name: string,
  prefix: string,
): DriverArtifact {
  const uart = json.protocol.bus === "UART";
  const can = json.protocol.bus === "CAN";
  const noAddrBus = uart || can; // neither UART nor CAN has a bus device address
  const addr = hexOrUndefined(json.protocol.addresses?.[0]);
  const crc = commands.find((c) => c.crc)?.crc;
  const guard = `${prefix}_H`;
  const type = `${name}_t`;
  const seamTable = busSeam(name);

  const header: string[] = [
    AUTOGEN(
      name,
      json.metadata.part || name,
      json.metadata.manufacturer,
      uart
        ? "Bus: UART · command-set device"
        : can
          ? "Bus: CAN · command-set device"
          : `Bus: I2C, address ${addr ?? "(unknown — see TODO)"} · command-set device`,
    ),
    "",
    `#ifndef ${guard}`,
    `#define ${guard}`,
    "",
    "#include <stddef.h>",
    "#include <stdint.h>",
    "",
    '#ifdef __cplusplus',
    'extern "C" {',
    "#endif",
    "",
  ];

  if (noAddrBus) {
    // No bus device address on UART/CAN — only I2C command-set parts get an
    // *_I2C_ADDR macro (see fill_in_brief.framing_todo for the UART/CAN
    // reasoning gap this leaves instead).
  } else if (addr) {
    header.push("/* I2C device address. */", `#define ${prefix}_I2C_ADDR ${addr}`, "");
  } else {
    header.push(
      "/* TODO(driverge): set the I2C device address. */",
      `#define ${prefix}_I2C_ADDR 0x00`,
      "",
    );
  }

  if (commands.length === 0) {
    header.push(...commandSetTodo(prefix, json.extraction?.detectedPages ?? []));
  } else {
    header.push("/* Command codes. */");
    const seen = new Set<string>();
    for (const c of commands) {
      const macroName = `${prefix}_CMD_${macro(c.name)}`;
      if (seen.has(macroName)) continue;
      seen.add(macroName);
      const line = `#define ${macroName} ${c.code.toUpperCase().replace("0X", "0x")}`;
      // c.params is currently always hex-derived (prose-commands.ts) and thus
      // safe as-is, but the Command type doesn't guarantee that — commentSafe
      // defensively, same reasoning as AUTOGEN/bitFieldMacros above (B2).
      header.push(
        c.params && c.params.length > 0
          ? `${line}  /* params: ${commentSafe(c.params.join(", "))} */`
          : line,
      );
    }
  }

  // A non-hex crc.poly/init (e.g. containing an embedded newline) must never
  // reach a #define as a live value (define-injection fix) — only emit the
  // block when BOTH are well-formed hex literals; skip it entirely otherwise
  // (crc8Body/commandBrief still reference the macro NAMES, not the raw
  // values, so no injected content survives as code either way).
  if (crc && hexOrUndefined(crc.poly) && hexOrUndefined(crc.init)) {
    header.push(
      "",
      `/* CRC-${crc.width} checksum parameters. */`,
      `#define ${prefix}_CRC_POLY ${crc.poly}`,
      `#define ${prefix}_CRC_INIT ${crc.init}`,
    );
  }

  header.push(
    "",
    "/* Thin-HAL seam — implement these for your platform (see thin-hal). */",
    ...(uart ? seamTable.UART.decl : can ? seamTable.CAN.decl : seamTable.I2C.decl),
    `void ${name}_hal_delay_ms (uint32_t ms);`,
    "",
    "/* Driver handle. */",
    "typedef struct {",
    uart ? seamTable.UART.handleField : can ? seamTable.CAN.handleField : seamTable.I2C.handleField,
    `} ${type};`,
    "",
    "/* Public API. */",
    `int ${name}_init(${type} *dev);`,
    `int ${name}_send_command(${type} *dev, uint16_t command);`,
    `int ${name}_read_data(${type} *dev, uint8_t *buffer, uint16_t len);`,
    ...(crc ? [`uint8_t ${name}_crc8(const uint8_t *data, uint16_t len);`] : []),
    "",
    '#ifdef __cplusplus',
    "}",
    "#endif",
    `#endif /* ${guard} */`,
    "",
  );

  const source: string[] = [
    `#include "${name}.h"`,
    "",
    `int ${name}_init(${type} *dev) {`,
    "    if (dev == NULL) {",
    "        return -1;",
    "    }",
    ...(noAddrBus ? [] : [`    dev->i2c_addr = ${prefix}_I2C_ADDR;`]),
    ...COMMAND_INIT_TODO,
    "    return 0;",
    "}",
    "",
    `int ${name}_send_command(${type} *dev, uint16_t command) {`,
    ...(uart
      ? uartFramingBody(name, ["dev", "command"])
      : can
        ? canFramingBody(name, ["dev", "command"])
        : [
            "    uint8_t msb;",
            "    uint8_t lsb;",
            "    if (dev == NULL) {",
            "        return -1;",
            "    }",
            "    msb = (uint8_t)(command >> 8);",
            "    lsb = (uint8_t)(command & 0xFF);",
            ...I2C_COMMAND_FRAME_NOTE,
            `    return ${name}_hal_i2c_write(dev->i2c_addr, msb, &lsb, 1);`,
          ]),
    "}",
    "",
    `int ${name}_read_data(${type} *dev, uint8_t *buffer, uint16_t len) {`,
    ...(uart
      ? uartFramingBody(name, ["dev", "buffer", "len"])
      : can
        ? canFramingBody(name, ["dev", "buffer", "len"])
        : [
            "    if (dev == NULL || buffer == NULL) {",
            "        return -1;",
            "    }",
            ...commandReadDataTodo(`${name}_crc8`),
            "    (void)dev;",
            "    (void)buffer;",
            "    (void)len;",
            "    return 0;",
          ]),
    "}",
    "",
  ];

  if (crc) {
    source.push(...crc8Body(`${name}_crc8`, crc, prefix));
  }

  const brief = commandBrief(json, commands, crc, uart, can, name, prefix);
  return { files: makeFiles(name, header, source), fill_in_brief: brief };
}

/**
 * Names the command_set fill_in_brief prose uses for the driver's own entry
 * points. Defaults to the C free-function names; the cpp generator passes the
 * class method names instead (Session D: cpp language option) — see
 * RegisterBriefCalls for the register_map analogue.
 */
export interface CommandBriefCalls {
  sendCommand: string;
  readData: string;
  crc8: string;
}

export function commandBrief(
  json: DatasheetJson,
  commands: Command[],
  crc: Command["crc"] | undefined,
  uart: boolean,
  can: boolean,
  name: string,
  prefix: string,
  calls?: Partial<CommandBriefCalls>,
): FillInBrief {
  const c: CommandBriefCalls = {
    sendCommand: calls?.sendCommand ?? `${name}_send_command`,
    readData: calls?.readData ?? `${name}_read_data`,
    crc8: calls?.crc8 ?? `${name}_crc8`,
  };
  const brief: FillInBrief = {
    init_sequence_todo: `Determine the power-up / soft-reset sequence and any startup delay for ${json.metadata.part || "the device"}.`,
    quirks_todo: uart
      ? `Verify the response checksum algorithm from the datasheet before trusting ${c.readData}'s output — vendor checksum/CRC methods vary by part.`
      : can
        ? CAN_QUIRKS_NOTE
        : `Implement ${c.readData} for this part's actual read framing (clock stretching vs. fixed delay, number of data+CRC words per command) using the driverge://datasheet resource.`,
    doc_todo: "Add Doxygen comments for all public functions and document each command's response layout.",
  };
  if (crc) {
    brief.crc_todo = `Implement ${c.crc8}: CRC-${crc.width}, polynomial ${crc.poly}, init ${crc.init}, per the datasheet checksum section.`;
  }
  if (commands.length === 0) {
    brief.command_set_todo = commandSetTodoText(json, name, prefix);
  }
  if (uart) {
    brief.framing_todo = `Implement ${name}'s UART frame protocol in ${c.sendCommand}/${c.readData}: build request frames with ${name}_hal_uart_write and parse responses with ${name}_hal_uart_read, per the datasheet's communication/frame-format section.`;
  }
  if (can) {
    brief.framing_todo = `Implement ${name}'s CAN frame protocol in ${c.sendCommand}/${c.readData}: build request frames and call ${name}_hal_can_transfer() with this part's arbitration id(s), per the datasheet's CAN communication/message-ID section.`;
  }

  return brief;
}

