// Portable thin-HAL codegen. Renders a deterministic C driver skeleton from the
// frozen DatasheetJson: register/command constants, bit-field mask+shift macros,
// the per-bus thin-HAL seam (I2C: <slug>_hal_i2c_read/write + <slug>_hal_delay_ms;
// SPI: one combined <slug>_hal_spi_transfer + <slug>_hal_delay_ms — see busSeam
// below and thin-hal-non-negotiable) and NOTHING platform-specific, function
// stubs, and TODO(driverge) markers at every spot needing host-AI reasoning.
// Every thin-HAL seam symbol is PER-DRIVER PREFIXED (`<slug>_hal_*`, slug =
// slug(json.metadata.part)) — see decisions: per-driver-seam-prefixing. A bare
// `hal_*` name collides at link the moment a project has two Driverge drivers
// (see raw/stm32-test-results/*.md); the prefix makes every driver's seam
// symbol set disjoint from every other driver's. Output is reproducible: no
// timestamps, stable ordering. The host AI fills the brief and re-validates via
// validate_driver.

import { registerWidth, type Register } from "../pdf/types.js";
import type { Command, DatasheetJson } from "../schema/types.js";
import { HEX_ONLY, commentSafe, fieldMask, hexOrUndefined, macro, maskHex, slug } from "./ident.js";
import type { DriverArtifact, FillInBrief, GeneratedFile } from "./types.js";

// Exported: the "cpp" language flavor (see portable-cpp.ts) renders the SAME
// #define macro / TODO blocks and thin-HAL seam declarations as this C
// generator — reusing these pure helpers instead of re-deriving them is how the
// two flavors stay byte-for-byte consistent on everything except the
// struct-vs-class/free-function-vs-method shape (see Session D: cpp language
// option).
// part/mfr/busLine are free text lifted verbatim from the parsed datasheet
// (metadata.part/manufacturer; busLine's only free-text ingredient is the I2C
// address, which is hex-validated, but commentSafe-ing the whole trio here —
// once, in the one place every AUTOGEN caller shares — is simpler and safer
// than re-deriving which piece is "safe enough" at each call site) — see B2:
// comment-escape injection. commentSafe neutralizes any embedded `*/`/`/*` so
// none of the three can ever prematurely close (or spoof) this block comment.
// `name` is the seam slug (never free text — see ident.ts's slug()) so it is
// embedded directly with no commentSafe-ing needed.
export const AUTOGEN = (name: string, part: string, mfr: string, busLine: string): string =>
  [
    "/*",
    ` * ${commentSafe(part)} driver — portable thin-HAL skeleton (generated by Driverge).`,
    ` * Manufacturer: ${commentSafe(mfr)}`,
    ` * ${commentSafe(busLine)}`,
    " *",
    " * This is a deterministic skeleton. Every TODO(driverge) marks a spot the",
    " * host AI must complete (init sequence, vendor quirks, docs) using the",
    " * datasheet JSON, then confirm with the validate_driver tool.",
    " *",
    ` * Portability rule: this driver calls ONLY its own ${name}_hal_* seam below —`,
    " * never a vendor peripheral API directly. Implement the seam once per",
    " * platform. The seam is per-driver prefixed so multiple Driverge drivers",
    " * in one project never collide at link (see thin-hal-non-negotiable).",
    " */",
  ].join("\n");

