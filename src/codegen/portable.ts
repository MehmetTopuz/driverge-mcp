// ---------------------------------------------------------------------------
// bus -> thin-HAL seam registry
// ---------------------------------------------------------------------------
//
// Each supported register_map bus contributes: the seam declaration line(s)
// (rendered in the header, right below the "Thin-HAL seam" comment), the
// driver-handle struct field, and the read_register/write_register bodies.
// <slug>_hal_delay_ms is common to every bus and stays outside this table.
// Session C adds a CAN row here — keep each row self-contained so a new bus
// never touches the others.
//
// busSeam(name) builds this table FRESH per driver (rather than a static
// Record) because every declared function name, and every readBody/writeBody
// call, must embed `name` (the seam slug) — see the module header comment
// (per-driver seam prefixing). `name` is never free text (ident.ts's slug()
// only emits [a-z0-9_]), so it is safe to splice directly into both generated
// C identifiers and RegExp sources (lint.ts) with no further escaping.
//
// UART and CAN are the odd rows out: neither has a universal register-access
// primitive — UART is just a byte pipe, and CAN register/config access is
// device-specific (CANopen SDO, J1939 PGNs, raw message-ID schemes) — so
// read_register/write_register have no honest implementation here. Their
// readBody/writeBody are therefore unused (see framingBody below, which
// renders a TODO(driverge) framing gap directly in
// registerDriver/commandDriver instead of a seam call) — kept as empty arrays
// purely so BusSeam's shape stays uniform across buses.

export type RegisterMapBus = "I2C" | "SPI" | "UART" | "CAN";

interface BusSeam {
  /** Lines appended after the "Thin-HAL seam" header comment, in order. */
  decl: string[];
  /** Field(s) for the driver handle typedef struct. */
  handleField: string;
  /** Body of <name>_read_register (dev, reg, value already in scope). Unused for UART/CAN. */
  readBody: string[];
  /** Body of <name>_write_register (dev, reg, value already in scope). Unused for UART/CAN. */
  writeBody: string[];
}

/**
 * Per-driver-prefixed thin-HAL seam table. See the section header comment
 * above for why this is a builder (keyed on `name`) rather than a static
 * Record — every declared symbol and every call site below embeds `name`.
 */
export function busSeam(name: string): Record<RegisterMapBus, BusSeam> {
  return {
    I2C: {
      decl: [
        "/* Return 0 on success, non-zero on a bus error (e.g. NACK). */",
        `int ${name}_hal_i2c_write(uint8_t addr, uint8_t reg, uint8_t *data, uint16_t len);`,
        `int ${name}_hal_i2c_read (uint8_t addr, uint8_t reg, uint8_t *data, uint16_t len);`,
      ],
      handleField: "    uint8_t i2c_addr;",
      readBody: [`    return ${name}_hal_i2c_read(dev->i2c_addr, reg, value, 1);`],
      writeBody: [`    return ${name}_hal_i2c_write(dev->i2c_addr, reg, &value, 1);`],
    },
    // Session E: full-duplex contract (see the field-test plan's finding B).
    // One call = one CS-framed, full-duplex transaction; rx may be NULL for a
    // write-only transfer. Most register reads are the half-duplex
    // write-then-read idiom over that full-duplex primitive: pad tx with a
    // dummy 0x00 byte after the address so the response lands at the matching
    // rx offset (rx[0] is garbage clocked in while tx[0]=reg goes out, rx[1]
    // is the value) — see readBody below. TUSS4470-style same-frame-response
    // (genuinely full-duplex) devices have the host AI fill tx/rx directly in
    // a custom TODO(driverge) spot without touching this seam signature.
    SPI: {
      decl: [
        "/* One call = one CS-framed, full-duplex transaction: CS is asserted, len",
        " * bytes are clocked out from tx while len bytes are simultaneously clocked",
        " * in to rx, then CS is deasserted. rx may be NULL for a write-only",
        " * transfer (tx is still fully clocked out). Returns 0 on success,",
        " * non-zero on a bus error.",
        " *",
        " * Half-duplex write-then-read idiom (most register reads): pad tx with a",
        " * dummy 0x00 byte after the register address so the device's response",
        " * lands at the matching rx offset — e.g. tx = {reg, 0x00}; rx[0] is",
        " * garbage (clocked in while tx[0]=reg goes out) and rx[1] is the",
        " * register's value. */",
        `int ${name}_hal_spi_transfer(const uint8_t *tx, uint8_t *rx, uint16_t len);`,
      ],
      handleField: `    uint8_t _reserved; /* platform CS handled in ${name}_hal_spi_transfer */`,
      readBody: [
        "    uint8_t tx[2];",
        "    uint8_t rx[2];",
        "    int status;",
        "    tx[0] = reg;",
        "    tx[1] = 0x00;",
        `    status = ${name}_hal_spi_transfer(tx, rx, 2);`,
        "    *value = rx[1];",
        "    return status;",
      ],
      writeBody: [
        "    uint8_t tx[2];",
        "    tx[0] = reg;",
        "    tx[1] = value;",
        `    return ${name}_hal_spi_transfer(tx, NULL, 2);`,
      ],
    },
    UART: {
      decl: [
        `/* ${name}_hal_uart_write is a blocking write of len bytes. */`,
        `/* ${name}_hal_uart_read reads up to len bytes and returns the count`,
        " * actually read within timeout_ms. */",
        `void ${name}_hal_uart_write(const uint8_t *data, uint16_t len);`,
        `uint16_t ${name}_hal_uart_read(uint8_t *data, uint16_t len, uint32_t timeout_ms);`,
      ],
      handleField: `    uint8_t _reserved; /* platform UART handle is bound inside the ${name}_hal_uart_* implementation */`,
      readBody: [],
      writeBody: [],
    },
    // Session C — CAN. Unlike I2C's two-function read/write pair or SPI's single
    // transfer, one <name>_hal_can_transfer() call both sends a frame AND
    // (optionally) waits for the one response frame it provokes — CAN has no
    // separate read/write primitive, only "put a frame on the bus, addressed
    // by arbitration id, and maybe get one back". rx_len is in/out (capacity
    // in, actual byte count out) because a CAN payload is 0-8 bytes, not a
    // fixed size.
    CAN: {
      decl: [
        "/* A call sends one CAN frame to arbitration id `id` and optionally waits",
        " * for one response frame (pass rx/rx_len NULL/0 to only transmit).",
        " * Returns 0 on success. rx_len is in/out: in = buffer capacity, out =",
        " * bytes actually received. */",
        `int ${name}_hal_can_transfer(uint32_t id, const uint8_t *tx, uint8_t tx_len, uint8_t *rx, uint8_t *rx_len, uint32_t timeout_ms);`,
      ],
      handleField: `    uint8_t _reserved; /* platform CAN peripheral/arbitration-id binding is handled inside the ${name}_hal_can_transfer implementation */`,
      readBody: [],
      writeBody: [],
    },
  };
}

/** Which busSeam row a protocol.bus resolves to. "unknown" defaults to the I2C
 *  seam, matching the pre-existing (pre-Session-B) fallback behavior. */
export function seamBusFor(bus: string): RegisterMapBus {
  if (bus === "SPI") return "SPI";
  if (bus === "UART") return "UART";
  if (bus === "CAN") return "CAN";
  return "I2C";
}

// UART and CAN have no universal register/command framing (start bytes, IDs,
// checksums, arbitration ids are all device-specific — see decisions:
// thin-hal-non-negotiable), so rather than fabricate a wire format, the
// generated body is a deliberate TODO(driverge) reasoning gap over the raw
// seam call(s): a compilable placeholder (params voided, returns -1) that
// names the PREFIXED seam function(s) so the host AI knows exactly what to
// build on top of. See fill_in_brief.framing_todo.
export function uartFramingBody(name: string, params: string[]): string[] {
  return [
    "    /* TODO(driverge): this device has no universal UART register/command",
    `     * framing — build the request frame and call ${name}_hal_uart_write(),`,
    `     * then parse the response via ${name}_hal_uart_read(). See`,
    "     * fill_in_brief.framing_todo. */",
    ...params.map((p) => `    (void)${p};`),
    "    return -1;",
  ];
}

export function canFramingBody(name: string, params: string[]): string[] {
  return [
    "    /* TODO(driverge): this device has no universal CAN register/command",
    `     * framing — build the CAN frame and call ${name}_hal_can_transfer() with`,
    "     * this part's arbitration id to send it (and receive any response). See",
    "     * fill_in_brief.framing_todo. */",
    ...params.map((p) => `    (void)${p};`),
    "    return -1;",
  ];
}

// CAN-specific reasoning gap shared by both interface kinds: which arbitration
// id / SDO index / message ID maps to which register or command is
// device-specific and belongs in fill_in_brief.quirks_todo, NOT a generic
// checksum note reused from UART.
export const CAN_QUIRKS_NOTE =
  "Confirm the arbitration IDs / SDO message schema this part uses for register/command access from the datasheet; CAN device interfaces are device-specific.";

export function bitFieldMacros(prefix: string, registers: Register[]): string[] {
  const lines: string[] = [];
  for (const r of registers) {
    if (r.bitFields.length === 0) continue;
    const width = registerWidth(r);
    const widthNote = width > 8 ? ` (${width}-bit register)` : "";
    // r.name is free text lifted verbatim from the parsed datasheet — see B2:
    // comment-escape injection — so it must not be allowed to close this
    // block comment early via an embedded `*/`.
    lines.push("", `/* ${commentSafe(r.name)} bit fields${widthNote}. */`);
    for (const f of r.bitFields) {
      const base = `${prefix}_${macro(r.name)}_${macro(f.name)}`;
      lines.push(`#define ${base}_MASK  ${maskHex(fieldMask(f.msb, f.lsb, width), width)}`);
      lines.push(`#define ${base}_SHIFT ${f.lsb}`);
    }
  }
  return lines;
}

function registerDriver(
  json: DatasheetJson,
  registers: Register[],
  name: string,
  prefix: string,
): DriverArtifact {
  const busKind = seamBusFor(json.protocol.bus);
  const spi = busKind === "SPI";
  const uart = busKind === "UART";
  const can = busKind === "CAN";
  const seam = busSeam(name)[busKind];
  const addr = hexOrUndefined(json.protocol.addresses?.[0]);
  const guard = `${prefix}_H`;
  const type = `${name}_t`;

  const header: string[] = [
    AUTOGEN(
      name,
      json.metadata.part || name,
      json.metadata.manufacturer,
      spi
        ? "Bus: SPI"
        : uart
          ? "Bus: UART"
          : can
            ? "Bus: CAN"
            : `Bus: I2C, address ${addr ?? "(unknown — see TODO)"}`,
    ),
    "",
    `#ifndef ${guard}`,
    `#define ${guard}`,
    "",
    "#include <stddef.h>",
    "#include <stdint.h>",
    "",
    '#ifdef __cplusplus',
    'extern "C" {',
    "#endif",
    "",
  ];

  if (!spi && !uart && !can) {
    if (addr) {
      header.push("/* I2C device address. */", `#define ${prefix}_I2C_ADDR ${addr}`, "");
    } else {
      header.push(
        "/* TODO(driverge): set the I2C device address (not found in the datasheet parse). */",
        `#define ${prefix}_I2C_ADDR 0x00`,
        "",
      );
    }
  }

  const regLines = registerConstants(prefix, registers);
  const hasRegs = regLines.some((l) => l.startsWith("#define"));
  header.push(
    ...(hasRegs
      ? regLines
      : registerMapTodo(prefix, json.extraction?.detectedPages ?? [])),
  );
  if (registers.some((r) => registerWidth(r) > 8)) {
    header.push(
      "",
      "/* NOTE: some registers here are wider than 8 bits (see the per-register",
      " * annotations). Bit-field masks are register-width correct; multi-byte access",
      " * framing (byte order / transfer size) is device-specific and belongs on the",
      " * hal_* seam. */",
    );
  }
  const bits = bitFieldMacros(prefix, registers);
  if (bits.length > 0) header.push("", "/* Bit-field mask/shift accessors. */", ...bits);

  header.push(
    "",
    "/* Thin-HAL seam — implement these for your platform (see thin-hal). */",
    ...seam.decl,
    `void ${name}_hal_delay_ms (uint32_t ms);`,
    "",
    "/* Driver handle. */",
    `typedef struct {`,
    seam.handleField,
    `} ${type};`,
    "",
    "/* Public API. */",
    `int ${name}_init(${type} *dev);`,
    `int ${name}_read_register(${type} *dev, uint8_t reg, uint8_t *value);`,
    `int ${name}_write_register(${type} *dev, uint8_t reg, uint8_t value);`,
    "",
    '#ifdef __cplusplus',
    "}",
    "#endif",
    `#endif /* ${guard} */`,
    "",
  );

  // The I2C/SPI seam bodies now END with `return <slug>_hal_..._transfer/read/write(...)`,
  // propagating the seam's status (0 = success) to the caller — a NACK/bus error
  // is no longer swallowed (A7). So no trailing `return 0` is appended here.
  const readBody = uart
    ? uartFramingBody(name, ["dev", "reg", "value"])
    : can
      ? canFramingBody(name, ["dev", "reg", "value"])
      : ["    if (dev == NULL || value == NULL) {", "        return -1;", "    }", ...seam.readBody];
  const writeBody = uart
    ? uartFramingBody(name, ["dev", "reg", "value"])
    : can
      ? canFramingBody(name, ["dev", "reg", "value"])
      : ["    if (dev == NULL) {", "        return -1;", "    }", ...seam.writeBody];

  const source: string[] = [
    `#include "${name}.h"`,
    "",
    `int ${name}_init(${type} *dev) {`,
    "    if (dev == NULL) {",
    "        return -1;",
    "    }",
    ...(spi || uart || can ? [] : [`    dev->i2c_addr = ${prefix}_I2C_ADDR;`]),
    ...registerInitTodo(name, uart, can, `${name}_write_register`),
    "    return 0;",
    "}",
    "",
    `int ${name}_read_register(${type} *dev, uint8_t reg, uint8_t *value) {`,
    ...readBody,
    "}",
    "",
    `int ${name}_write_register(${type} *dev, uint8_t reg, uint8_t value) {`,
    ...writeBody,
    "}",
    "",
  ];

  const brief = registerBrief(json);
  if (!hasRegs) {
    brief.register_map_todo = registerMapTodoText(json, name, prefix);
  }
  return {
    files: makeFiles(name, header, source),
    fill_in_brief: brief,
  };
}

/**
 * Names the register_map fill_in_brief prose uses to refer to the driver's own
 * init/read/write entry points. Defaults to the C free-function names
 * (`<name>_init`, `read_register`/`write_register`); the cpp generator passes
 * the class method names instead so the SAME brief builder produces
 * language-accurate text without duplicating the quirks/framing prose (Session
 * D: cpp language option).
 */
export interface RegisterBriefCalls {
  init: string;
  readRegister: string;
  writeRegister: string;
}

export function registerBrief(
  json: DatasheetJson,
  calls?: Partial<RegisterBriefCalls>,
): FillInBrief {
  const part = json.metadata.part || "the device";
  const name = slug(part);
  const c: RegisterBriefCalls = {
    init: calls?.init ?? `${name}_init`,
    readRegister: calls?.readRegister ?? "read_register",
    writeRegister: calls?.writeRegister ?? "write_register",
  };
  const quirks = [
    `Capture vendor timing/behavior quirks for ${part} (e.g. mandatory power-on delay, soft-reset requirement, read-back constraints) and enforce them in ${c.init} using ${name}_hal_delay_ms.`,
  ];
  if (json.protocol.bus === "SPI") {
    quirks.push(
      `Verify the register address bit convention: many SPI chips set the address MSB for reads (e.g. 0x80 | reg) — check the datasheet's SPI framing and adjust ${c.readRegister}/${c.writeRegister}.`,
    );
  }
  if (json.protocol.bus === "UART") {
    quirks.push(
      "Verify the response checksum algorithm from the datasheet before trusting frame data — vendor checksum/CRC methods for UART command-and-response parts vary.",
    );
  }
  if (json.protocol.bus === "CAN") {
    quirks.push(CAN_QUIRKS_NOTE);
  }
  const brief: FillInBrief = {
    init_sequence_todo: `Determine the correct power-on/reset init sequence for ${part}: which registers to write, in what order, with what values (mode/config), and any required startup delay. Read the datasheet init/operation section via the driverge://datasheet resource.`,
    quirks_todo: quirks.join(" "),
    doc_todo:
      "Add Doxygen comments for all public functions and any device-specific compensation/conversion formulas.",
  };
  if (json.protocol.bus === "UART") {
    brief.framing_todo = `Implement ${name}'s UART frame protocol in ${c.readRegister}/${c.writeRegister}: build request frames with ${name}_hal_uart_write and parse responses with ${name}_hal_uart_read, per the datasheet's communication/frame-format section.`;
  }
  if (json.protocol.bus === "CAN") {
    brief.framing_todo = `Implement ${name}'s CAN frame protocol in ${c.readRegister}/${c.writeRegister}: build request frames and call ${name}_hal_can_transfer() with this part's arbitration id(s), per the datasheet's CAN communication/message-ID section.`;
  }
  return brief;
}

