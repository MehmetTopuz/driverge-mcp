// STM32 (CubeHAL) target. Like ESP32, a native target pre-fills the thin-HAL
// seam rather than replacing it: the driver is the unchanged portable core plus a
// <slug>_hal_stm32.c that implements the bus's thin-HAL seam + <slug>_hal_delay_ms
// with STM32 CubeHAL (HAL_I2C_Mem_Read/Write, HAL_SPI_TransmitReceive/Transmit +
// a CS GPIO, or HAL_UART_Transmit/Receive, plus HAL_Delay), PLUS a
// <slug>_hal_stm32.h companion header declaring the <slug>_stm32_bind()
// prototype the seam .c defines. The user binds their peripheral handle(s) once
// via <slug>_stm32_bind() (after #include-ing the companion header); the
// board/clock bring-up stays in their CubeMX project.
//
// Session E (2026-07-11 field-test findings — see raw/stm32-test-results/*.md
// and the approved plan): every seam symbol is PER-DRIVER PREFIXED
// (`<slug>_hal_*` — see busSeam in portable.ts), so two Driverge drivers in one
// STM32 project never collide at link (the TUSS4470/CAP1206 field failure —
// see tests/codegen/multi-driver-link-gate.test.ts). The SPI seam is also now a
// single full-duplex <slug>_hal_spi_transfer(tx, rx, len) — one CS window, one
// HAL_SPI_TransmitReceive (or HAL_SPI_Transmit when rx is NULL) — matching the
// portable core's contract exactly.
//
// I2C/SPI/UART only: the I2C seam hard-codes CubeHAL's HAL_I2C_Mem_* calls and
// references `${PREFIX}_I2C_ADDR`; the SPI seam needs the portable core's
// <slug>_hal_spi_transfer declaration; the UART seam (Session B) wraps
// HAL_UART_Transmit/Receive around <slug>_hal_uart_write/<slug>_hal_uart_read.
// Any other bus (CAN, unknown) has no CubeHAL seam here, so this target refuses
// (UnsupportedBusError) rather than emit an uncompilable seam — bxCAN/FDCAN
// support is a dedicated future session.

import type { DatasheetJson } from "../schema/types.js";
import { prefixOf, slug } from "./ident.js";
import { generatePortableCppDriver } from "./portable-cpp.js";
import { generatePortableDriver } from "./portable.js";
import { UnsupportedBusError } from "./types.js";
import type { CodegenLanguage, DriverArtifact, GeneratedFile } from "./types.js";

// Contract amendment (post-GREEN 4b3da14/d576ba6): a language:"cpp" seam
// cannot stay byte-identical to the "c" seam, because that file
// #include's "<slug>.h" — which doesn't exist in a cpp bundle (only
// <slug>.hpp/.cpp). So with language "cpp" the seam file itself becomes
// `<slug>_hal_<target>.cpp` and its core include becomes `#include
// "<slug>.hpp"`; everything else in the seam is unchanged. This costs
// nothing: the .hpp declares the hal_* seam inside `extern "C"`, so compiling
// this seam as C++ still emits plain C-linkage definitions the class methods
// can call, and a C++-enabled CubeMX/CubeIDE project compiles .cpp sources
// natively. The seam companion header (Session E) stays a plain `.h` file in
// BOTH language runs — its own `extern "C"` guard makes it valid from either a
// C or a C++ translation unit, so there is nothing to switch on `language` for
// (see companionHeaderStm32 below).
function coreInclude(name: string, language: CodegenLanguage): string {
  return `#include "${name}.${language === "cpp" ? "hpp" : "h"}"`;
}

function seamPath(name: string, language: CodegenLanguage): string {
  return `${name}_hal_stm32.${language === "cpp" ? "cpp" : "c"}`;
}

function companionHeaderName(name: string): string {
  return `${name}_hal_stm32.h`;
}

/**
 * Session E: the seam companion header. Declares the `<slug>_stm32_bind(...)`
 * prototype the seam .c/.cpp defines — CAP1206/TUSS4470/FXL6408 field
 * sessions all had to hand-write this prototype into their own application
 * code (raw/stm32-test-results/*.md §6) because Driverge never emitted it.
 * `bindProtoLine` is the exact signature (identical to the `void
 * <slug>_stm32_bind(...) {` line the seam defines, just with a trailing `;`
 * instead of a body) — passed in by each halImplX below so the header and the
 * definition can never drift.
 */
function companionHeaderStm32(name: string, prefix: string, bindProtoLine: string): GeneratedFile {
  const guard = `${prefix}_HAL_STM32_H`;
  const content = [
    "/*",
    ` * ${prefix} — STM32 CubeHAL seam companion header (generated by Driverge).`,
    " *",
    ` * Declares ${name}_stm32_bind(), which ${seamPathBase(name)} defines. Include`,
    " * this header from your application code (e.g. main.c) to call it after",
    " * peripheral bring-up.",
    " */",
    `#ifndef ${guard}`,
    `#define ${guard}`,
    "",
    '#include "main.h"',
    "",
    "#ifdef __cplusplus",
    'extern "C" {',
    "#endif",
    "",
    bindProtoLine,
    "",
    "#ifdef __cplusplus",
    "}",
    "#endif",
    `#endif /* ${guard} */`,
    "",
  ].join("\n");
  return { path: companionHeaderName(name), content };
}

/** `<slug>_hal_stm32` — the seam's base filename, independent of language
 *  extension, purely for the companion header's own prose. */
function seamPathBase(name: string): string {
  return `${name}_hal_stm32.c/.cpp`;
}

function halImplI2c(name: string, prefix: string, language: CodegenLanguage): [GeneratedFile, GeneratedFile] {
  const content = [
    "/*",
    ` * ${prefix} — STM32 CubeHAL seam implementation (generated by Driverge).`,
    " *",
    " * Drop this beside the portable core; it fills the thin-HAL seam with STM32",
    ` * CubeHAL. See the companion header (${companionHeaderName(name)}) for the`,
    ` * ${name}_stm32_bind() prototype — call it once at startup with your`,
    " * CubeMX-generated I2C handle (e.g. &hi2c1) so the seam knows which",
    " * peripheral to use.",
    " */",
    coreInclude(name, language),
    `#include "${companionHeaderName(name)}"`,
    "",
    `#ifndef ${prefix}_I2C_TIMEOUT_MS`,
    `#define ${prefix}_I2C_TIMEOUT_MS 1000`,
    "#endif",
    "",
    `static I2C_HandleTypeDef *s_${name}_hi2c;`,
    "",
    "/* Point the seam at the CubeMX-generated I2C handle (e.g. &hi2c1). */",
    `void ${name}_stm32_bind(I2C_HandleTypeDef *hi2c) {`,
    `    s_${name}_hi2c = hi2c;`,
    "}",
    "",
    `int ${name}_hal_i2c_write(uint8_t addr, uint8_t reg, uint8_t *data, uint16_t len) {`,
    "    /* CubeHAL takes the 8-bit (left-shifted) device address. */",
    `    HAL_StatusTypeDef status = HAL_I2C_Mem_Write(s_${name}_hi2c, (uint16_t)(addr << 1), reg,`,
    `        I2C_MEMADD_SIZE_8BIT, data, len, ${prefix}_I2C_TIMEOUT_MS);`,
    "    return (status == HAL_OK) ? 0 : -1;",
    "}",
    "",
    `int ${name}_hal_i2c_read(uint8_t addr, uint8_t reg, uint8_t *data, uint16_t len) {`,
    `    HAL_StatusTypeDef status = HAL_I2C_Mem_Read(s_${name}_hi2c, (uint16_t)(addr << 1), reg,`,
    `        I2C_MEMADD_SIZE_8BIT, data, len, ${prefix}_I2C_TIMEOUT_MS);`,
    "    return (status == HAL_OK) ? 0 : -1;",
    "}",
    "",
    `void ${name}_hal_delay_ms(uint32_t ms) {`,
    "    HAL_Delay(ms);",
    "}",
    "",
  ].join("\n");
  const header = companionHeaderStm32(name, prefix, `void ${name}_stm32_bind(I2C_HandleTypeDef *hi2c);`);
  return [header, { path: seamPath(name, language), content }];
}

function halImplSpi(name: string, prefix: string, language: CodegenLanguage): [GeneratedFile, GeneratedFile] {
  const content = [
    "/*",
    ` * ${prefix} — STM32 CubeHAL SPI seam implementation (generated by Driverge).`,
    " *",
    " * Drop this beside the portable core; it fills the thin-HAL seam with STM32",
    " * CubeHAL SPI + a GPIO chip-select: one full-duplex transaction (one CS",
    ` * window) per ${name}_hal_spi_transfer() call. See the companion header`,
    ` * (${companionHeaderName(name)}) for the ${name}_stm32_bind() prototype —`,
    " * call it once at startup with your CubeMX-generated SPI handle and CS",
    " * GPIO pin (e.g. &hspi1, CS_GPIO_Port, CS_Pin).",
    " */",
    coreInclude(name, language),
    `#include "${companionHeaderName(name)}"`,
    "",
    `#ifndef ${prefix}_SPI_TIMEOUT_MS`,
    `#define ${prefix}_SPI_TIMEOUT_MS 1000`,
    "#endif",
    "",
    `static SPI_HandleTypeDef *s_${name}_hspi;`,
    `static GPIO_TypeDef *s_${name}_cs_port;`,
    `static uint16_t s_${name}_cs_pin;`,
    "",
    "/* Point the seam at the CubeMX-generated SPI handle (e.g. &hspi1) and the CS",
    " * GPIO pin (e.g. CS_GPIO_Port, CS_Pin). */",
    `void ${name}_stm32_bind(SPI_HandleTypeDef *hspi, GPIO_TypeDef *cs_port, uint16_t cs_pin) {`,
    `    s_${name}_hspi = hspi;`,
    `    s_${name}_cs_port = cs_port;`,
    `    s_${name}_cs_pin = cs_pin;`,
    "}",
    "",
    "/* Full-duplex: HAL_SPI_TransmitReceive when a response is wanted (rx != NULL),",
    " * HAL_SPI_Transmit for a write-only transfer (rx == NULL) — either way, ONE",
    " * CS window around the whole transfer. */",
    `int ${name}_hal_spi_transfer(const uint8_t *tx, uint8_t *rx, uint16_t len) {`,
    "    HAL_StatusTypeDef status;",
    `    HAL_GPIO_WritePin(s_${name}_cs_port, s_${name}_cs_pin, GPIO_PIN_RESET);`,
    "    if (rx != NULL) {",
    `        status = HAL_SPI_TransmitReceive(s_${name}_hspi, (uint8_t *)tx, rx, len, ${prefix}_SPI_TIMEOUT_MS);`,
    "    } else {",
    `        status = HAL_SPI_Transmit(s_${name}_hspi, (uint8_t *)tx, len, ${prefix}_SPI_TIMEOUT_MS);`,
    "    }",
    `    HAL_GPIO_WritePin(s_${name}_cs_port, s_${name}_cs_pin, GPIO_PIN_SET);`,
    "    return (status == HAL_OK) ? 0 : -1;",
    "}",
    "",
    `void ${name}_hal_delay_ms(uint32_t ms) {`,
    "    HAL_Delay(ms);",
    "}",
    "",
  ].join("\n");
  const header = companionHeaderStm32(
    name,
    prefix,
    `void ${name}_stm32_bind(SPI_HandleTypeDef *hspi, GPIO_TypeDef *cs_port, uint16_t cs_pin);`,
  );
  return [header, { path: seamPath(name, language), content }];
}

function halImplUart(name: string, prefix: string, language: CodegenLanguage): [GeneratedFile, GeneratedFile] {
  const content = [
    "/*",
    ` * ${prefix} — STM32 CubeHAL UART seam implementation (generated by Driverge).`,
    " *",
    " * Drop this beside the portable core; it fills the thin-HAL seam with STM32",
    ` * CubeHAL UART. See the companion header (${companionHeaderName(name)}) for`,
    ` * the ${name}_stm32_bind() prototype — call it once at startup with your`,
    " * CubeMX-generated UART handle (e.g. &huart1).",
    " */",
    coreInclude(name, language),
    `#include "${companionHeaderName(name)}"`,
    "",
    `#ifndef ${prefix}_UART_TIMEOUT_MS`,
    `#define ${prefix}_UART_TIMEOUT_MS 1000`,
    "#endif",
    "",
    `static UART_HandleTypeDef *s_${name}_huart;`,
    "",
    "/* Point the seam at the CubeMX-generated UART handle (e.g. &huart1). */",
    `void ${name}_stm32_bind(UART_HandleTypeDef *huart) {`,
    `    s_${name}_huart = huart;`,
    "}",
    "",
    `void ${name}_hal_uart_write(const uint8_t *data, uint16_t len) {`,
    `    HAL_UART_Transmit(s_${name}_huart, (uint8_t *)data, len, ${prefix}_UART_TIMEOUT_MS);`,
    "}",
    "",
    `uint16_t ${name}_hal_uart_read(uint8_t *data, uint16_t len, uint32_t timeout_ms) {`,
    "    HAL_StatusTypeDef status;",
    `    status = HAL_UART_Receive(s_${name}_huart, data, len, timeout_ms);`,
    "    if (status == HAL_OK) {",
    "        return len;",
    "    }",
    "    /* Timeout/error: CubeHAL stops short of `len` bytes and leaves the number",
    "     * of bytes it still expected in RxXferCount, so (len - RxXferCount) is",
    "     * the count actually received before the timeout fired. */",
    `    return (uint16_t)(len - s_${name}_huart->RxXferCount);`,
    "}",
    "",
    `void ${name}_hal_delay_ms(uint32_t ms) {`,
    "    HAL_Delay(ms);",
    "}",
    "",
  ].join("\n");
  const header = companionHeaderStm32(name, prefix, `void ${name}_stm32_bind(UART_HandleTypeDef *huart);`);
  return [header, { path: seamPath(name, language), content }];
}

/**
 * Portable core + a CubeHAL seam implementation + a seam companion header for
 * the same part. With language "c" (default) the seam is `<slug>_hal_stm32.c`;
 * with language "cpp" it is `<slug>_hal_stm32.cpp` and its core include is
 * `#include "<slug>.hpp"` (see coreInclude/seamPath above) — everything else
 * in the seam content is identical either way. The companion header
 * (`<slug>_hal_stm32.h`) is always a plain `.h` file, in BOTH language runs.
 * File order in the artifact: [core header, core source, companion header,
 * seam source] — see tests/codegen/stm32.test.ts.
 */
export function generateStm32Driver(
  json: DatasheetJson,
  opts: { language?: CodegenLanguage } = {},
): DriverArtifact {
  const bus = json.protocol.bus;
  if (bus !== "I2C" && bus !== "SPI" && bus !== "UART") {
    throw new UnsupportedBusError("stm32", bus);
  }
  const name = slug(json.metadata.part);
  const prefix = prefixOf(json.metadata.part);
  const language = opts.language ?? "c";
  const base = language === "cpp" ? generatePortableCppDriver(json) : generatePortableDriver(json);

  const [companionHeader, hal] =
    bus === "SPI"
      ? halImplSpi(name, prefix, language)
      : bus === "UART"
        ? halImplUart(name, prefix, language)
        : halImplI2c(name, prefix, language);
  const headerName = companionHeaderName(name);
  const hal_setup_todo =
    bus === "SPI"
      ? `Configure the SPI peripheral and a CS GPIO output pin in CubeMX (mode/clock per the datasheet's SPI timing section), then call ${name}_stm32_bind(&hspiX, CS_GPIO_Port, CS_Pin) once at startup (prototype in ${headerName}). Adjust ${prefix}_SPI_TIMEOUT_MS if needed.`
      : bus === "UART"
        ? `Configure the USART/UART peripheral in CubeMX (pins, baud rate, parity, stop bits per the datasheet's serial interface section), then call ${name}_stm32_bind(&huartX) once at startup (prototype in ${headerName}). Adjust ${prefix}_UART_TIMEOUT_MS if needed.`
        : `Configure the I2C peripheral in CubeMX (pins, clock, 7-bit addressing), then call ${name}_stm32_bind(&hi2cX) once at startup (prototype in ${headerName}). Adjust ${prefix}_I2C_TIMEOUT_MS if needed.`;

  return {
    files: [...base.files, companionHeader, hal],
    fill_in_brief: {
      ...base.fill_in_brief,
      hal_setup_todo,
    },
  };
}
